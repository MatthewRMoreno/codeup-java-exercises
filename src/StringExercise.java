public class StringExercise {
    public static <function> void main(String[] args) {
        String statement = "We don't need no education. We don't need no thought control";
        String statement2 = "Check \"this\" out!, \"s inside of \"s!";
        String statement3 = "In windows, the main drive is usually C:\\";
        String statement4 = "I can do backslashes \\, double backslashes \\\\,\n" +
                "and the amazing triple backslash \\\\\\!";
        System.out.println(statement);
        System.out.println(statement2);
        System.out.println(statement3);
        System.out.println(statement4);
    }
}
